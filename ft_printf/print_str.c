#include "ft_printf.h"

int	print_str(char *str)
{
	int	i;

	i = 0;
	if (!str)
	{
		print_str("(null)");
		return (6);
	}
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
	return (i);
}
